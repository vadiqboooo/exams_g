async def import_subjects():
    """Импортирует предметы в базу данных"""
    async with AsyncSessionLocal() as db:
        print("Начало импорта предметов...")

        for code, data in SUBJECTS_DATA.items():
            # Проверяем, существует ли предмет
            result = await db.execute(select(Subject).where(Subject.code == code))
            existing = result.scalar_one_or_none()

            if existing:
                print(f"  Предмет '{code}' уже существует, пропускаем")
                continue

            # Создаем новый предмет
            subject = Subject(
                code=code,
                name=data['name'],
                exam_type=data['exam_type'],
                tasks_count=data['tasks'],
                max_per_task=data['maxPerTask'],
                primary_to_secondary_scale=data.get('scale'),
                special_config=data.get('special_config'),
                topics=None,  # Темы можно будет добавить позже через веб-интерфейс
                is_active=True,
                created_at=datetime.now(timezone.utc).replace(tzinfo=None),
                updated_at=datetime.now(timezone.utc).replace(tzinfo=None)
            )

            db.add(subject)
            print(f"  [+] Добавлен предмет: {data['name']} ({code})")

        await db.commit()
        print("\nИмпорт завершен!")


if __name__ == "__main__":
    asyncio.run(import_subjects())
